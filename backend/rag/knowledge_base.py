def split_text(text: str, chunk_size: int = 300, chunk_overlap: int = 50) -> List[str]:
    """Small local splitter to keep RAG optional dependencies out of app startup."""
    if len(text) <= chunk_size:
        return [text]

    chunks = []
    start = 0
    while start < len(text):
        end = min(start + chunk_size, len(text))
        chunks.append(text[start:end])
        if end == len(text):
            break
        start = max(0, end - chunk_overlap)
    return chunks


def load_knowledge_documents() -> List[Dict[str, str]]:
    """加载知识库并切分为可向量化的文档片段。每个元素为 {"content": "...", "metadata": {"title": "...", "category": "..."}}。"""
    documents = []

    for entry in KNOWLEDGE_ENTRIES:
        chunks = split_text(entry["content"])
        for i, chunk in enumerate(chunks):
            documents.append({
                "content": chunk,
                "metadata": {
                    "title": entry["title"],
                    "category": entry["category"],
                    "chunk_index": i,
                },
            })

    logger.info(f"加载 {len(KNOWLEDGE_ENTRIES)} 条知识，切分为 {len(documents)} 个文档片段")
    return documents


def get_knowledge_summary() -> List[Dict[str, str]]:
    """返回知识库概览，用于展示或调试。"""
    return [
        {"title": e["title"], "category": e["category"], "content_len": len(e["content"])}
        for e in KNOWLEDGE_ENTRIES
    ]
